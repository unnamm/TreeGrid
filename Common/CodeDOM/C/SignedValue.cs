namespace Common.CodeDOM.C
{
    public class SignedValue : CodeValue
	{
		private long value;

		public SignedValue(long value) : base(value) { }

		public long Value
		{
			get { return value; }
			set
			{
				ValueObject = value;
				this.value  = value;
			}
		}
	}
}